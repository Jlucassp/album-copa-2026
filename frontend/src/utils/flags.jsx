export function FlagIcon({ teamCode, size = "sm" }) {
  const iso = teamCodeToISO[teamCode];
  if (!iso) return null;

  const sizeClass = size === "sm" ? "w-5 h-4" : "w-6 h-5";

  return (
    <span
      className={`fi fi-${iso} ${sizeClass} rounded-sm inline-block`}
      style={{ backgroundSize: "cover" }}
    />
  );
}
